// ARITHMETIC OPERATION 
let a = 4;
let b = 7;

console.log("a + b = ",a + b);//add
console.log("a - b = ",a - b);//sub
console.log("a * b = ",a * b);//multiply
console.log("a / b = ",a / b);//divide 
console.log("a % b = ",a % b); // modulo (reminder)
console.log("a ** b = ",a ** b); //power


// UNARY

//pre increment
console.log("pre increment =",++a);
console.log("pre increment =",--a);

//post increment
console.log("post increment =",a++);
console.log("post increment =",a--);

